package ru.alesya0711.laba6.dao.interfaces;

import java.util.List;

/**
 * Интерфейс доступа к данным для управления связью между студентами и учебными группами.
 *
 * <p>Предоставляет методы для получения списка идентификаторов студентов,
 * входящих в указанную группу.
 */
public interface StudentsGroupDAO {

    /**
     * Возвращает список уникальных идентификаторов студентов, зачисленных в указанную группу.
     *
     * @param groupId уникальный идентификатор учебной группы
     * @return список ID студентов; пустой список, если группа пуста или не найдена
     * @throws RuntimeException если произошла ошибка при выполнении SQL-запроса
     */
    List<Long> getStudentIdsByGroupId(Long groupId);
}